  <div class="article-title">
    <p style="font-size: 4.2vw;">Reklamowanie w mediach społecznościowych z użyciem płatnych reklam</p>
  </div>
  <div class="article">
    <p>
      Reklama z użyciem płatnych reklam to temat w związku z którym napisano nie jeden artykuł w sieci. W tym poście, w kilku krokach, postaram się wyszczególnić 
      to co najbadziej kluczowe w tworzeniu odnoszących sukcesy - i co najważniejsze - przyciągających do reklamowanej marki klientów kampanii reklamowych.
    </p>
    <h3 class="text-center" style="padding-bottom: 2rem;">
      Więc jak to działa?
    </h3>
    <p>
      Płatne reklamy w mediach społecznościowych mają na celu przekierować potencjalnego klienta na Twój profil w mediach społecznościowych, stronę internetową 
      czy też specjalnie do tego celu utworzony - "landing page" zawierający informacje o Twojej ofercie wraz z nacechowaną persfazją zachętą do podjęcia akcji (zakupu). 
      Tyle z terorii działania. Co jest zatem potrzebne by osiągnąć taki rezultat? W naszym zespole stosujemy poniższą procedurę.
    </p>
    <p>
      <strong>
        1. Zrozumienie swojego klienta (odpowiednie stargetowanie reklam pozwala nam uniknąć tzw "przepalenia budżetu reklamowego"). </br>
      </strong>
      Pytania które warto sobie tu zadać to: 
      <ul style="font-size: 18px;">
        <li>
          Kim jest klient chętny do skorzystania z moich usług czy produktów? 
        </li>
        <li>
          Czy to kobieta, mężczyzna czy jedno i drugie? 
        </li>
        <li>
          W jakim jest wieku?
        </li>
        <li>
          Jeśli sprzedajemy usługe to jak daleko mieszka od miejsca jej wykonywania?
        </li>
        <li>
          Czy w wybranej lokalizacji jest tylko przejazdem czy w niej mieszka?
        </li>
        <li>
          Czym osoba zainteresowana danym produktem czy usługą może się zajmować?
        </li>
        <li>
          Jaka jest jej profesja?
        </li>
        <li>
          Jakie są jej zainteresowania?
        </li>
        <li>
          Jaki jest problem który rozwiązują moje produkty czy usługi? (odpowiedż na te pytanie jest często prawdziwym złotem)
        </li>
      </ul>
    </p>
    <p>
      Lista pytań może się jeszcze ciągnąć i ciągnąć w zależności od tego ile wariantów jesteśmy skłonni przewidzieć i co ważniejsze wziąć pod uwagę. 
      Generalnie, na im większą ilość pytań uzyskamy odpowiedzi - tym lepiej będziemy w stanie stargetować naszą reklamę. </br>
      Do zadawania istotnych pytań bardzo przydaje się wiedza dotycząca psychologii, socjologii oraz bahawiorystyki. 
    </p>
    <p>
      <strong>
        2. Wykorzystanie copywritingu do stworzenia persfazyjnego tekstu służącego do skłonienia potncjalnego klienta do kliknięcia w reklame. </br>
      </strong>
      O copywritingu pisałem szerzej w innym poście. 
      <a href="/blog/what-is-copywriting" target="_blank">
        Kliknij by dowiedzieć się więcej.
      </a>
    </p>
    <p>
      <strong>
        3. Wykorzystanie programów do edycji grafiki i video by stowrzyć część wizualną reklamy. </br>
      </strong>
      Etap ten to nic innego jak dobór odpowiednich grafik czy materiałów filmowych, edycja ich oraz naniesienie na nie napisów. 
    </p>
    <p>
      <strong>
        4. Umiejętność obsługi kont reklamowych w serwisach społecznościowych. </br>
      </strong>
      Nie oszukujmy się, co z tego, że wiemy kim jest nasz klient, jeśli poruszanie się bo mediach społecznościowych wydaje nam się niekomfortową wyprawą w nieznane. 
      Dla kogoś kto nie jest laikiem może zabrzmieć to trywialnie lecz osoby które na codzień skupiają się wyłącznie na swojej pracy, w konfrontacji z obsługą i konfiguracją 
      serwisów mogą czuć się przynajmniej skołowane. </br>
    </p>
    <p>
      <strong>
        5. Tworzenie testowych grup reklam w celu wyłonienia wariantu rokującego najlepiej. </br>
      </strong>
      Etap czwarty wiąże się bezpośrednio z etapem 4 jednak tworzenie testowych grup reklam wymaga poświęcenia dodatkowej uwagi ponieważ wymaga kilkukrotnego stworzenia tekstów
       sprzedażowych oraz grafik czy nagrań video. Wszystko w celu zrobienia kilku zestawów reklam które będzie można wypuścić na dzień czy dwa w celu wybrania faworyta generującego
       największy ruch. Następnym krokiem jest wyłączenie gorzej działających reklam i przeznaczenie pozostałego budżetu reklamowego na faworyta.
    </p>
