    <p style="font-size: 1.2rem;">
      Prowadzenie kont w serwisach społecznościowych to proces skłądający się z wielu czynników.
    </p>
    <p>
      <strong>
        1. Najrozsądniej zacząć od obrania swojego rodzaju misji. </br>
      </strong>
      Na misje składają się dobór charakteru przekazu treści oraz ogólny styl prowadzenia konta. 
      Warto odpowiedzieć tu sobie na pytania takie jak np: 
    </p>
    <ul style="font-size: 18px;">
      <li>
        do kogo kierujemy nasze treści,
      </li>
      <li>
        kto jest klientem docelowym,
      </li>
      <li>
        jaka treść sprawi, żę klient z zainteresowaniem będzie śledził to co publikujemy w naszych postach oraz polecał je innym osobom. 
      </li>
    </ul>
    <p>
      Co kluczowe - trzeba tu myśleć w kategoriach dawania wartości w postaci ciekawostek 
      czy wartościowej wiedzy która może przydać się Twojemu klientowi oraz spawi, że w jego oczach będziesz prezentować się jako autorytet!
    </p>
    <p>
      Wspaniale sprawdzają się również różnego rodzaju konkursy czy głosowania dzięki którym w zamian za uczestnictwo, komentarz czy opublikowanie na swojej tablicy, rozdaje się jakieś nagrody czy zniżki. 
    </p>
    <p>
      <strong>
        2. Dostosowanie przekazu do wybranego serwisu społecznościowego. </br>
      </strong> 
      Każdy z serwisów społecznościowych jest inny. Każdy z nich posiada użytkowników oczekujących treści prezentowanych w odpowiedniej dla nich formie. 
      Chodzi o to, że to co przyciągnie uwagę na Twitterze niekoniecznie dobrze zadziała na Instagramie. 
      Platformy te są od siebie niekiedy skrajnie różne co też warto mieć z tyłu głowy podczas tworzenia treści. Prowadzi to nas do kolejnego punktu jakim jest...
    </p>
    <p>
      <strong>
        3. Wiedza o copywritingu czyli w skrócie umiejętność pisania persfazyjnych tekstów sprzedażowych. </br>
      </strong>
      O copywritingu pisałem szerzej w innym poście.
      <a href="/blog/what-is-copywriting" target="_blank">
        Kliknij by dowiedzieć się więcej. 
      </a>
    </p>
    <p>
      <strong>
        4. Umiejętności związane z obróbką materiałów zdjęciowych oraz video. </br>
      </strong>
      Kolejnym krokiem jest zrobienie postów. Do ich stworzenia, niejednokrotnie trzeba będzie korzystać z umiejętności związanych z edycją zdjęć, grafik czy montowaniem filmów. 
      Do tego celu najlepiej sprawdzają się licencjonowane pogramy graficzne. 
    </p>
    <p>
      <strong>
        5. Zwiększanie zasięgów kluczowych postów przy użyciu płatnych reklam. </br>
      </strong>
      Jest to swoista wisienka na torcie, czynność której nie stosuję większość Twojej konkurencji. </br>
      Poprzez promocje postów z konkursami oraz odpowiednie stargetowanie odbiorców tych reklam zwiększamy świadomość konsumencką oraz poprawiamy widoczność konta w danym serwisie 
      (wszystkie serwisy społecznościowe bazują głównie na danych związanych z liczbą odwiedzin stron, ilości komentarzy pozostawianych pod postami oraz ich udostępnieniami (same lajki oceniane są najniżej)). 
      Konsekwencją takich zabiegów będzie drastycznie zwiększenie szansy na to, że potencjalny klient - szukając potrzebnego produktu czy usługi - zdecyduję się na kontakt właśnie z Twoją firmą! 
    </p>
